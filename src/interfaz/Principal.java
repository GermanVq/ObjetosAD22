    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
      int op ,e1 ,e2, n1 , n2, d1 , d2;
      Mixto m1 , m2 ,m3 = null;
     
       
     if(txtNum1.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite numerador numero 1","Error", JOptionPane.ERROR_MESSAGE);
         txtNum1.requestFocusInWindow();
     }else if(txtDen1.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite denominador numero 1","Error", JOptionPane.ERROR_MESSAGE);
         txtDen1.requestFocusInWindow();
     }else if(txtNum2.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite numerador numero 2","Error", JOptionPane.ERROR_MESSAGE);
         txtNum2.requestFocusInWindow();
     }else if(txtDen2.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite denominador numero 2","Error",JOptionPane.ERROR_MESSAGE);
         txtDen2.requestFocusInWindow();
         }else if(txtEntero1.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite Entero numero1","Error",JOptionPane.ERROR_MESSAGE);
         txtEntero1.requestFocusInWindow();
         }else if(txtEntero2.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this,"Digite Entero numero 2","Error",JOptionPane.ERROR_MESSAGE);
         txtEntero2.requestFocusInWindow();
     }else{
         
     }
       op= cmbOperacion.getSelectedIndex();
       e1=Integer.parseInt(txtEntero1.getText());
       n1=Integer.parseInt(txtNum1.getText());
       d1=Integer.parseInt(txtDen1.getText());
       e2=Integer.parseInt(txtEntero2.getText());
       n2=Integer.parseInt(txtNum2.getText());
       d2=Integer.parseInt(txtDen2.getText());
       
       m1= new Mixto(e1,n1,d1);
       m2= new Mixto(e2,n2,d2);
       
       switch(op){
           case 0:
               m3 = m1.Sumar(m2);
               break;
           case 1:
               m3 = m1.Resta(m2);
               break;
           case 2:
               m3=m1.Multiplicacion(m2);
               break;
           case 3:
               m3=m1.Division(m2);
       }
       
       txtEntero3.setText(""+m3.getEntero());
       txtNum3.setText(""+m3.getNumerador());
       txtDen3.setText(""+m3.getDenominador());
     
    

   
        
      
  
    }//GEN-LAST:event_cmdCalcularActionPerformed

